    private void cargarComboBoxMedicamento() {
         
        try {
            Connection conn = datos.Coneccion.Enlace();
            ResultSet rs = conn.createStatement().executeQuery("Select * from MEDICAMENTO");

            while (rs.next()) {

                Medicamento m = new Medicamento(rs.getInt("ID_MCTOS"), rs.getString("NOMBRE_REGISTRADO"), rs.getString("FORMA_FARMACEUTICA"), rs.getString("DOSIS"), rs.getDate("CADUCIDAD"), rs.getString("INDICACIONES"), rs.getInt("FARMACIA_ID"));
                medicamentos.add(m);
            }

            for (Medicamento m : medicamentos) {
                nombres.add(m.getNombreRegistrado());
            }

            ModeloComboBox model = new ModeloComboBox();
            model.setModelo(nombres);
            model.setSelectedItem(model.getElementAt(0));

            jeliminar.setModel(model);

        } catch (SQLException ex) {
            Logger.getLogger(Eliminar.class.getName()).log(Level.SEVERE, null, ex);
        }
        catch(IndexOutOfBoundsException e){
            JOptionPane.showMessageDialog(null, "NO QUEDAN MEDICAMENTOS");
 
            
        }
    }

