    private void btnConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultaActionPerformed
       
        try {
            Connection conn = datos.Coneccion.Enlace();
            String[]titulos = {"NOMBRE REGISTRADO","FORMA FARMACEUTICA","DOSIS","CADUCIDAD","INDICACIONES"};
            String consulta = "select * from MEDICAMENTO";
            DefaultTableModel model = new DefaultTableModel(null,titulos);
            Statement sent = conn.createStatement();
            ResultSet rs = sent.executeQuery(consulta);
            
            String []fila = new String[5];
            
            while(rs.next()){
                fila[0]=rs.getString("NOMBRE_REGISTRADO");
                fila[1]=rs.getString("FORMA_FARMACEUTICA");
                fila[2]=rs.getString("DOSIS");
                fila[3]=rs.getDate("CADUCIDAD").toString();
                fila[4]=rs.getString("INDICACIONES");
                
                model.addRow(fila);
            }
            jTable1.setModel(model);
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        
    }//GEN-LAST:event_btnConsultaActionPerformed

